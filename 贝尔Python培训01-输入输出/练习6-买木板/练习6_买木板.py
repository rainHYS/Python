'''
[编程实现]
小明家是卖木板的，每块木板单价20。每次客人来买木板爸爸都需要用计算器输入卖的木板数量和单价计算出总价，然后还需要用计算器计算找零，爸爸觉得麻烦，想请小明写一个程序，只需要输入客人买的木板数量和客人交的钱，就可以算出客人需要付多少钱，及需要退给客人多少钱。
[输入格式]
2行。第一行代表客人买的木板数量；第二行代表客人一共给了多少钱（还未找零时）
[输出格式]
一行：一共a元。找零b元
a代表客人需要给多少钱；b代表需要退给客人多少钱
[样例输入]
23
5000
[样例输出]
一共460元。找零4540元
'''
a=int(input())
b=int(input())
print("一共",a*20,"元。找零",b-a*20,"元")

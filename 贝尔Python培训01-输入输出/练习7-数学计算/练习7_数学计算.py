'''
[编程实现]
输入两个整数，分别计算两个整数的和，差，积，商和余数，乘方。（注意：每一行的符号之间不能有空格）
[输入格式]
两行，每行一个数字
[输出格式]
五行，分别是加、减、乘、除、乘方的计算式以及结果
[样例输入]
6
3
[样例输出]
6+3=9
6-3=3
6*3=18
6/3=2...0
6**3=216
'''
num1=int(input())
num2=int(input())
print(num1,"+",num2,"=",num1+num2)
print(num1,"-",num2,"=",num1-num2)
print(num1,"*",num2,"=",num1*num2)
print(num1,"/",num2,"=",int(num1/num2),"...",num1%num2)
print(num1,"**",num2,"=",num1**num2)